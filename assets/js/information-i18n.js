(function initInfoI18n() {
  const html = document.documentElement;
  const langBtnEn = document.getElementById('langBtnEn');
  const langBtnUr = document.getElementById('langBtnUr');
  let currentLang = localStorage.getItem('selectedLanguage') || 'en';

  const t = (key, lang) => (infoTranslations[key] ? infoTranslations[key][lang] : '');

  function translatePage(lang) {
    document.querySelectorAll('[data-i18n]').forEach((el) => {
      const key = el.getAttribute('data-i18n');
      if (infoTranslations[key]) el.textContent = t(key, lang);
    });

    document.querySelectorAll('.nav-links a, .nav-mobile a').forEach((link) => {
      const href = link.getAttribute('href') || '';
      if (href.includes('index.html')) link.textContent = t('navbar-dashboard', lang);
      if (href.includes('information.html')) link.textContent = t('navbar-information', lang);
      if (href.includes('specialist.html')) link.textContent = t('navbar-specialist', lang);
    });

    document.querySelectorAll('#severity .badge[data-i18n]').forEach((badge) => {
      badge.textContent = t(badge.getAttribute('data-i18n'), lang);
    });

    const dropdown = document.getElementById('sectionDropdown');
    if (dropdown) {
      const val = dropdown.value;
      const keys = ['dropdown-1', 'dropdown-2', 'dropdown-3', 'dropdown-4', 'dropdown-5', 'dropdown-6', 'dropdown-7'];
      const anchors = ['#what', '#cobb', '#severity', '#types', '#treatment', '#warning', '#disclaimer'];
      dropdown.innerHTML = '<option value="">' + t('dropdown-placeholder', lang) + '</option>';
      keys.forEach((k, i) => {
        const opt = document.createElement('option');
        opt.value = anchors[i];
        opt.textContent = t(k, lang);
        dropdown.appendChild(opt);
      });
      dropdown.value = val;
    }
  }

  function setLang(lang) {
    const content = document.querySelector('.info-content');
    if (content) content.style.opacity = '0';

    setTimeout(() => {
      currentLang = lang;
      localStorage.setItem('selectedLanguage', lang);

      if (lang === 'ur') {
        html.setAttribute('dir', 'rtl');
        html.setAttribute('lang', 'ur');
        html.classList.add('lang-ur');
        langBtnUr?.classList.add('lang-active');
        langBtnEn?.classList.remove('lang-active');
      } else {
        html.setAttribute('dir', 'ltr');
        html.setAttribute('lang', 'en');
        html.classList.remove('lang-ur');
        langBtnEn?.classList.add('lang-active');
        langBtnUr?.classList.remove('lang-active');
      }

      translatePage(lang);
      if (window.syncInfoPageLang) window.syncInfoPageLang(lang);
      if (content) content.style.opacity = '1';
    }, 250);
  }

  langBtnEn?.addEventListener('click', () => setLang('en'));
  langBtnUr?.addEventListener('click', () => setLang('ur'));

  if (currentLang === 'ur') {
    setLang('ur');
  } else {
    html.setAttribute('dir', 'ltr');
    html.setAttribute('lang', 'en');
    html.classList.remove('lang-ur');
    langBtnEn?.classList.add('lang-active');
    langBtnUr?.classList.remove('lang-active');
    translatePage('en');
  }
})();
